#include<iostream>
using namespace std;

int main()
{
 int a;
 cout<<"Enter the first number: \n";         //'<<' is called insertion operator
 cin>>a;                                     //'>>' is called an Extraction operator
                                                                         
 int b;
 cout<<"Enter the second number: \n"; 
 cin>>b;         
 
 cout<<"The sum is "<<a+b;    
 return 0;
}
